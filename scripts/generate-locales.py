# ============================================================
# STEP 1: Extract all t() calls with surrounding context
# ============================================================

def extract_t_calls(src_dir):
    """Extract all t("key") calls from source files with context."""
    calls = {}  # key -> { file, line_no, line_content, context_before, context_after }
    
    for root, dirs, files in os.walk(src_dir):
        dirs[:] = [d for d in dirs if d not in ("node_modules", ".git", "dist", ".qwen")]
        for fn in files:
            if fn.endswith((".tsx", ".ts")):
                path = os.path.join(root, fn)
                with open(path, "r", encoding="utf-8") as f:
                    lines = f.readlines()
                
                for i, line in enumerate(lines):
                    for m in re.finditer(
                        r'\bt\(["\']([a-zA-Z][a-zA-Z0-9]+(?:\.[a-zA-Z][a-zA-Z0-9]+)+)["\']',
                        line,
                    ):
                        key = m.group(1)
                        if key not in calls:
                            calls[key] = {
                                "file": path,
                                "line_no": i + 1,
                                "line_content": line.rstrip(),
                                "before": lines[max(0, i - 2) : i],
                                "after": lines[i + 1 : min(len(lines), i + 3)],
                            }
    return calls


